#include <stdio.h>

int main(void)
{
	//int arr[] = {};//这个数组没有意义
	//必须在声明数组的时候同时初始化
	//编译器会自动计算元素的个数
	int arr[] = {1,2,3,4,5,6,7};
	//int arr2[7] = arr;
	int arr2[7]; //假设这里的元素个数很大，arr2[20000],下面写20000的话，arr数组就太大了，浪费内存空间；写7的话要靠人工数，假设arr的元素有19000个，也数吗？会死人的哦？
	for (i = 0; i < 7; ++i)//这里的7是根据arr2的长度与arr的元素个数目测出来的。
	{
		arr2[i] = arr[i]; //只能确保arr2接收了7个数字
	}
	//下标越界
	//arr[70000000] = 10;
	return 0;
}

